package campionati;

public class Squadre {
	private String nome;
	private String stadio;
	private String presidente;

	
	public Squadre(String nome, String stadio, String presidente) {
		super();
		this.nome = nome;
		this.stadio = stadio;
		this.presidente = presidente;
	}

	public String getNome() {
		return nome;
	}

	public String getStadio() {
		return stadio;
	}

	public String getPresidente() {
		return presidente;
	}





}
